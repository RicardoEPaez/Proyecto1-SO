    public void actualizarMemoria(Memoria memoria) {
        // 1. Obtener datos del Backend
        PCB[] particiones = memoria.getParticiones(); // Necesitas haber creado este getter en Memoria.java
        int memLibre = memoria.getMemoriaDisponible();
        int memTotal = 1024; // Valor fijo según tu enunciado (o pásalo como variable)
        int memOcupada = memTotal - memLibre;
        
        // 2. Actualizar Labels y Barra
        lblEstado.setText("Uso: " + memOcupada + " MB / " + memTotal + " MB");
        
        // Si pusiste la barra de progreso:
        if (barraMemoria != null) {
            int porcentaje = (memOcupada * 100) / memTotal;
            barraMemoria.setValue(porcentaje);
            
            // Un toque de color: Rojo si está muy llena, Verde si está bien
            if (porcentaje > 80) {
                barraMemoria.setForeground(Color.RED);
            } else {
                barraMemoria.setForeground(Color.GREEN);
            }
        }

        // 3. Llenar la Tabla
        DefaultTableModel modelo = (DefaultTableModel) tbMemoria.getModel();
        modelo.setRowCount(0); // Limpiar tabla anterior
        
        for (int i = 0; i < particiones.length; i++) {
            if (particiones[i] != null) {
                // --- CASO: SLOT OCUPADO ---
                PCB p = particiones[i];
                modelo.addRow(new Object[]{
                    "Bloque " + i,
                    p.getId(),
                    p.getNombre(),
                    p.getTamano() + " MB",
                    "OCUPADO"
                });
            } else {
                // --- CASO: SLOT LIBRE ---
                modelo.addRow(new Object[]{
                    "Bloque " + i,
                    "---",
                    "---",
                    "---",
                    "LIBRE"
                });
            }
        }
    }

